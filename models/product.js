class Product {
    constructor(id, ownerId, title, imageUrl, description, price, barcode) {
        this.id = id;
        this.ownerId = ownerId;
        this.title = title;
        this.imageUrl = imageUrl;
        this.description = description;
        this.price = price;
        this.barcode = barcode;
    }
}

export default Product;
